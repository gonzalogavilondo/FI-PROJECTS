#include "declaraciones_funciones_reales.h"

float suma(float n1, float n2)
{
    float res;

    res = (n1 + n2);

    return res;
}

float resta(float n1, float n2)
{
    float res;

    res = (n1 - n2);

    return res;
}

float multiplicacion(float n1, float n2)
{
    float res;

    res = (n1*n2);

    return res;
}

float division(float n1, float n2)
{
    float res;

    res = (n1/n2);

    return res;
}
